package practise1;

public class incrementaloperator {
	public static void main(String[] args) {
		int x=10;
		int y=20;
		
		x++;//10+1=11
		System.out.println("x :" + x);
		++y;//1+20=21
		System.out.println("y :" + y);
		
		 int a=++x;//a=1+11
		 System.out.println("a :" + a);
		 int b=x++;//b=11+1=12
		 System.out.println("b:" + b);
		 int z=b++;//z=12+1=13
		 System.out.println("z:" + b);
		 int l=++b;//l=1+13=14
		 System.out.println("l:" + b);
		 
		 
		 
		 
		 
		
	}

}
